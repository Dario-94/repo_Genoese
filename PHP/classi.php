Classi

Una classe definisce la struttura di un oggetto

Un oggetto è una struttura che contiene:
	dati (proprietà)
	funzioni (metodi)

Definire una classe


<?php
	//classe solo con proprietà 
	//senza metodi
	class Indirizzo {
		public $name;
		public $cognome;
		public $indirizzo;
		public $tel;
}

?>
